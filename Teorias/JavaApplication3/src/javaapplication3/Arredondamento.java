public class Arredondamento {
    public static void main(String []args){
        System.out.println(Math.sqrt(49)); // raiz quadrada de 49
        System.out.println(Math.pow(5, 2)); // 5 elevado ao quadrado
        System.out.println(Math.PI); // valor de PI
        System.out.println(Math.abs(-10)); // valor absoluto
        System.out.println(Math.floor(4.6)); // pra baixo independente do valor
        System.out.println(Math.ceil(3.2)); // pra cima independente do valor
        System.out.println(Math.round(5.6)); // arredonda aritméticamente
        
    }
}


